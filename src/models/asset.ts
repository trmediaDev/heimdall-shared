import { ObjectId } from 'bson';
import { Type } from 'class-transformer';
import { IsArray, IsBoolean, IsEnum, IsOptional, IsString, ValidateNested } from 'class-validator';
import { Document } from 'mongodb';
import { AssetType } from './asset-type';
import { CategorySubcategory } from './categorysubcategory';

/// Asset Find request
export class AssetFindRequest {
    @IsEnum(AssetType)
    @IsOptional()
    type?: AssetType;

    @IsString()
    @IsOptional()
    filename?: string | RegExp;

    @IsString()
    @IsOptional()
    readableFilename?: string;

    @IsString()
    @IsOptional()
    originalFilename?: string;

    @IsString()
    @IsOptional()
    description?: string;

    @IsArray()
    @ValidateNested()
    @IsOptional()
    categorySubcategories?: CategorySubcategory[];
}

/// Asset Post request
export declare class AssetPostRequest {
    @IsEnum(AssetType)
    type: AssetType;

    @IsString()
    readableFilename: string;

    @IsString()
    @IsOptional()
    description?: string;
    @IsString()
    @IsOptional()
    mimeType?: string;

    @IsBoolean()
    @IsOptional()
    repetable?: boolean;
    @IsBoolean()
    @IsOptional()
    showInPublicSearch?: boolean;

    @IsArray()
    @IsOptional()
    keywords?: [];

    @IsString()
    @IsOptional()
    originalFilename?: string;
    @IsOptional()
    templateAssetCount?: number;
}

