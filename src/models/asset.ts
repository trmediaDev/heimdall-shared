export class Asset {
    @IsEnum(AssetType)
    type: AssetType;

    /**
     * This is used as the file name when storing the file on GCP
     * or on local disk. filename consist of mongo oid + extension
     */
    @IsString()
    filename: string;

    @IsString()
    readableFilename: string;

    @IsString()
    @IsOptional()
    originalFilename?: string;


    @IsString()
    @IsOptional()
    description?: string;

    @IsString()
    @IsOptional()
    mimeType?: string;

    @IsArray()
    keywords: string[];

    /** Right after an upload or file reload from GCS/S3 , this field will be generated by the server that handled the request */
    @Type(() => ObjectId)
    @IsOptional()
    cachedServerId?: ObjectId;

    @IsString()
    @IsOptional()
    cachedFilepath?: string;

    @IsString()
    @IsOptional()
    cachedThumbnailpath?: string;

    @IsString()
    @IsOptional()
    thumbnailName?: string;

    @IsBoolean()
    @IsOptional()
    repetable?: boolean;

    @IsBoolean()
    @IsOptional()
    archived?: boolean;

    @IsBoolean()
    @IsOptional()
    disabled?: boolean;

    @IsBoolean()
    @IsOptional()
    showInPublicSearch?: boolean;

    @IsOptional()
    templateAssetCount?: number;

    @IsString()
    @IsOptional()
    uploadedForTemplateId?: ObjectId;
    @IsString()
    @IsOptional()
    usedByTemplateIds?: ObjectId[];

    @IsString()
    @IsOptional()
    uploadedBy?: ObjectId;

    @IsBoolean()
    @IsOptional()
    modifiedBy?: ObjectId;

    @Type(() => Date)
    createdDate: Date;

    @Type(() => Date)
    updatedDate: Date;
    
}

export class AssetDBObject extends Asset {
    @Type(() => ObjectId)
    _id: ObjectId;
}

export class TempAssetDBObject extends Asset {
    @Type(() => ObjectId)
    _id: ObjectId;
    filePath: string;
}
